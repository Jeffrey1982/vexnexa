export function resolveReportLabels(acceptLanguage?: string | null, explicitLocale?: string | null): ReportLabels {
  const localeSource = explicitLocale || acceptLanguage || "en";
  const requested = localeSource.split(",")[0]?.split("-")[0]?.toLowerCase() as Locale;
  const locale: Locale = requested in messages ? requested : "en";
  const pdfLabels = (messages[locale] as any)?.scanReport?.pdf || {};

  return {
    ...DEFAULT_REPORT_LABELS,
    ...(BUILTIN_REPORT_TRANSLATIONS[locale] || {}),
    ...pdfLabels,
    locale,
  };
}
